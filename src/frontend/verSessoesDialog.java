    public void preencherTabela() {
        ArrayList<Sessao> sessoes = this.album.getSessoes();
        for(int i = 0; i < sessoes.size(); i++) {
            if(sessoes.get(i) != null) {
                //estado
                if(sessoes.get(i).isConclusao()){
                    tbSessoes.setValueAt("Concluida",i,0);
                }
                else{ 
                    tbSessoes.setValueAt("Nao concluida",i,0); 
                }
                //data agendada
                tbSessoes.setValueAt(sessoes.get(i).getDataI(),i,1);
                //data final
                if(sessoes.get(i).getDataf()!=null){
                    tbSessoes.setValueAt(sessoes.get(i).getDataf(),i,2);
                }
                else{
                    tbSessoes.setValueAt("Sem data",i,2);
                }
            }
        }
    }

