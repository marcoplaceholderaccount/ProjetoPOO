    private void VoltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VoltarActionPerformed
        // TODO add your handling code here:
        ArrayList<Sessao> sessoes = this.album.getSessoes();
        //verificar se todas as sessoes foram concluidas
        boolean todasConcluidas = true;
        for(Sessao sessao: sessoes){
            if(!sessao.isConclusao()){
                todasConcluidas = false;
                break;
            }
        }
        //se forem concluidas atribuir a data final ao album
        if(this.album.getDataFinal() == null){
            if(todasConcluidas){
                System.out.println("Todas as sessoes foram concluidas");
                this.album.setDataFinal();
                this.album.setEstado(true);
            }else{ 
                System.out.println("Ainda ha sessoes em andamento"); 
            }
        }
        else{
            System.out.println("A gravacao do album foi concluida");
        }
        //voltar e fechar a janela
        this.dispose();
    }//GEN-LAST:event_VoltarActionPerformed

